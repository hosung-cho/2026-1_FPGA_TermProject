def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--params", type=Path, default=Path("lenet_int8_params.h"))
    parser.add_argument("--out-dir", type=Path, default=Path("mem"))
    parser.add_argument("--docs-dir", type=Path, default=Path("docs"))
    parser.add_argument("--data-depth-words", type=int, default=16384)
    args = parser.parse_args()

    text = args.params.read_text(encoding="utf-8")
    args.out_dir.mkdir(parents=True, exist_ok=True)
    args.docs_dir.mkdir(parents=True, exist_ok=True)

    data = []
    rows = []

    for name, kind in PARAM_ORDER:
        align4(data)
        base_byte = len(data)

        if kind == "int8":
            values = parse_array(text, name)
            append_int8(data, values)
        elif kind == "int32":
            values = parse_array(text, name)
            append_int32_le(data, values)
        elif kind == "int32_scalar":
            values = parse_scalar(text, name)
            append_int32_le(data, values)
        else:
            raise ValueError(f"unknown kind: {kind}")

        rows.append(
            {
                "name": name,
                "type": kind,
                "base_byte": base_byte,
                "base_word": base_byte // 4,
                "byte_size": len(data) - base_byte,
                "element_count": len(values),
                "description": "",
            }
        )

    align4(data)
    words = bytes_to_words(data)

    write_mem(args.out_dir / "lenet_params.mem", words)
    write_csv(args.out_dir / "lenet_params_map.csv", rows)
    write_c_header(args.out_dir / "lenet_param_addr.h", rows)
    write_markdown(
        args.docs_dir / "Memory_Map.md",
        rows,
        total_bytes=len(data),
        total_words=len(words),
        data_depth_words=args.data_depth_words,
    )

    print(f"wrote={args.out_dir / 'lenet_params.mem'} words={len(words)} bytes={len(data)}")
    print(f"wrote={args.out_dir / 'lenet_params_map.csv'}")
    print(f"wrote={args.out_dir / 'lenet_param_addr.h'}")
    print(f"wrote={args.docs_dir / 'Memory_Map.md'}")


if __name__ == "__main__":
    main()
