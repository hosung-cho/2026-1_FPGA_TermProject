import argparse
import csv
import re
from pathlib import Path


PARAM_ORDER = [
    ("conv1_weight_int8", "int8"),
    ("conv1_bias_int32", "int32"),
    ("conv1_requant_multiplier", "int32_scalar"),
    ("conv2_weight_int8", "int8"),
    ("conv2_bias_int32", "int32"),
    ("conv2_requant_multiplier", "int32_scalar"),
    ("conv3_weight_int8", "int8"),
    ("conv3_bias_int32", "int32"),
    ("conv3_requant_multiplier", "int32_scalar"),
    ("fc1_weight_int8", "int8"),
    ("fc1_bias_int32", "int32"),
    ("fc1_requant_multiplier", "int32_scalar"),
    ("fc2_weight_int8", "int8"),
    ("fc2_bias_int32", "int32"),
    ("fc2_requant_multiplier", "int32_scalar"),
]


def parse_array(text, name):
    pattern = rf"static const (?:int8_t|int32_t) {name}(?:\[[^\]]+\])+ = \{{(.*?)\}};"
    match = re.search(pattern, text, re.S)
    if not match:
        raise ValueError(f"array not found: {name}")
    return [int(x) for x in re.findall(r"-?\d+", match.group(1))]


def parse_scalar(text, name):
    pattern = rf"static const int32_t {name} = (-?\d+);"
    match = re.search(pattern, text)
    if not match:
        raise ValueError(f"scalar not found: {name}")
    return [int(match.group(1))]


def align4(data):
    while len(data) % 4 != 0:
        data.append(0)


def append_int8(data, values):
    for value in values:
        data.append(value & 0xFF)


def append_int32_le(data, values):
    for value in values:
        word = value & 0xFFFFFFFF
        data.append(word & 0xFF)
        data.append((word >> 8) & 0xFF)
        data.append((word >> 16) & 0xFF)
        data.append((word >> 24) & 0xFF)


def bytes_to_words(data):
    words = []
    for i in range(0, len(data), 4):
        b0 = data[i]
        b1 = data[i + 1]
        b2 = data[i + 2]
        b3 = data[i + 3]
        words.append(b0 | (b1 << 8) | (b2 << 16) | (b3 << 24))
    return words


def write_mem(path, words):
    lines = ["@00000000"]
    lines.extend(f"{word:08x}" for word in words)
    path.write_text("\n".join(lines) + "\n", encoding="ascii")


def write_csv(path, rows):
    with path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(
            f,
            fieldnames=[
                "name",
                "type",
                "base_byte",
                "base_word",
                "byte_size",
                "element_count",
                "description",
            ],
        )
        writer.writeheader()
        writer.writerows(rows)


def write_c_header(path, rows):
    lines = [
        "/* Auto-generated by export_rv32i_mem.py */",
        "#ifndef LENET_PARAM_ADDR_H",
        "#define LENET_PARAM_ADDR_H",
        "",
    ]
    for row in rows:
        macro = row["name"].upper() + "_ADDR"
        lines.append(f"#define {macro} 0x{row['base_byte']:08X}")
    lines.extend(
        [
            "",
            "#define LENET_PARAM_TOTAL_BYTES "
            + f"0x{(rows[-1]['base_byte'] + rows[-1]['byte_size']):08X}",
            "",
            "#endif",
        ]
    )
    path.write_text("\n".join(lines) + "\n", encoding="ascii")


